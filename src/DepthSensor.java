/***************************************************************************
 * Depth Sensor Class
 **************************************************************************/
public class DepthSensor
{

    /***************************************************************************
     * Depth Sensor Constructor
     **************************************************************************/
    public DepthSensor()
    {}

    /***************************************************************************
     * getDepth()
     **************************************************************************/
    public double getDepth()
    {
        return 0.0;
    }

    /***************************************************************************
     * getDepth() - Simulator Overload
     **************************************************************************/
    public double getDepth(Simulator simulator)
    {
        return simulator.getDepthReading();
    }


}
